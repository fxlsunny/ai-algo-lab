// 对单个算法返回给定样本量下的资源需求
MCH.getResource = function (id, scale) {
  const p = MCH.resourceProfiles[id];
  if (!p) return null;
  const scaleLabel = typeof scale === "string" ? scale : String(scale);
  const train = p.train && (p.train[scaleLabel] || p.train["1亿"] || Object.values(p.train)[0]);
  return {
    paramsMin: p.params ? p.params[0] : 0,
    paramsMax: p.params ? p.params[1] : 0,
    paramsTypical: p.params ? Math.sqrt(p.params[0] * Math.max(1, p.params[1])) : 0,
    train,
    infer: p.infer || {},
  };
};
